package org.laladev.moneyjinn.sepa.camt.model;

import org.laladev.moneyjinn.sepa.camt.model.id.AbstractID;

public class Account {
	private AbstractID id;

	public Account() {
	}

	public Account(final AbstractID id) {
		super();
		this.id = id;
	}

	public final AbstractID getId() {
		return this.id;
	}

	public final void setId(final AbstractID id) {
		this.id = id;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((this.id == null) ? 0 : this.id.hashCode());
		return result;
	}

	@Override
	public boolean equals(final Object obj) {
		if (this == obj) {
			return true;
		}
		if (obj == null) {
			return false;
		}
		if (this.getClass() != obj.getClass()) {
			return false;
		}
		final Account other = (Account) obj;
		if (this.id == null) {
			if (other.id != null) {
				return false;
			}
		} else if (!this.id.equals(other.id)) {
			return false;
		}
		return true;
	}

	@Override
	public String toString() {
		final StringBuilder builder = new StringBuilder();
		builder.append("Account [id=");
		builder.append(this.id);
		builder.append("]");
		return builder.toString();
	}

}
